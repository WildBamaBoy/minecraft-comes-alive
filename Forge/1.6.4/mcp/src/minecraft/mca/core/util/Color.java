package mca.core.util;

/**
 * Contains color codes for messages displayed on screen.
 */
public final class Color 
{
	private static final String SIGN = "\u00a7";
	
	public static final String BLACK = SIGN + "0";
	public static final String DARKBLUE = SIGN + "1";
	public static final String DARKGREEN = SIGN + "2";
	public static final String DARKAQUA = SIGN + "3";
	public static final String DARKRED = SIGN + "4";
	public static final String PURPLE = SIGN + "5";
	public static final String GOLD = SIGN + "6";
	public static final String GRAY = SIGN + "7";
	public static final String DARKGRAY = SIGN + "8";
	public static final String BLUE = SIGN + "9";
	public static final String GREEN = SIGN + "A";
	public static final String AQUA = SIGN + "B";
	public static final String RED = SIGN + "C";
	public static final String LIGHTPURPLE = SIGN + "D";
	public static final String YELLOW = SIGN + "E";
	public static final String WHITE = SIGN + "F";
}
